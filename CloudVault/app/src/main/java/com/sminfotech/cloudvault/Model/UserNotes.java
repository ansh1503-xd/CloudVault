package com.sminfotech.cloudvault.Model;

public class UserNotes {

    String title;
    String body;

    public UserNotes() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
